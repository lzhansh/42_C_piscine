void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int j;

	j = argc - 1;
	while (0 < j)
	{
		i = 0;
		while (argv[j][i] != '\0')
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		ft_putchar('\n');
		j--;
	}
	return (0);
}
