int	ft_atoi(char *str)
{
	int res;
	int i;
	int s;

	i = 0;
	res = 0;
	s = 1;
	while (str[i] == '-' || str[i] == '+' || str[i] < '0' || str[i] > '9')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * s);
}
